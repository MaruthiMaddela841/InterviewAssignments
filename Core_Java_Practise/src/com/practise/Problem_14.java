public class Problem_14 {

	public static void main(String[] args) {

		int x = 97;
		switch (x) {
		case 98:
			System.out.println("97");
			break;
		case 99:
			System.out.println("99");
			break;
		case 'a':
			System.out.println("100"); // int x= 'a';
			break;
		}

	}

}
